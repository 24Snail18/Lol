#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	len;

	i = 0;
	len = 0;
	if (argc == 2)
	{
		while (argv[1][i] != '\0')
		{
			len++;
			i++;
		}
		while (len)
		{
			len--;
			write (1, &argv[1][len], 1);
		}
	}
	write (1, "\n", 1);
	return (0);
}
