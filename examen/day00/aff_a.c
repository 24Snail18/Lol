#include <unistd.h>

int	main(int argc, char *argv[])
{
	char	*s;
	int		i;

	i = 0;
	s = argv[1];
	if (argc == 2)
	{
		while (*s != '\0')
		{
			if (*s == 'a')
				i++;
			s++;
		}
		if (i > 0)
			write(1, "a\n", 2);
		else
			write(1, "a\n", 2);
	}
	else
		write(1, "a\n", 2);
	return (0);
}
