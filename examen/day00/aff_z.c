#include <unistd.h>

int	main(int argc, char *argv[])
{
	char	*s;
	int		i;

	s = argv [1];
	i = 0;
	if (argc != 2)
		write (1, "z\n", 2);
	else
	{
		while (*s != '\0')
		{
			if (*s == 'z')
				i++;
			s++;
		}
		if (i > 0)
			write (1, "z\n", 2);
		else
			write (1, "z\n", 2);
	}
	return (0);
}
