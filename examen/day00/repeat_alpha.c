#include <unistd.h>

int	main(int argc, char **argv)
{
	char	*s;
	int		i;

	i = 0;
	s = argv[1];
	if (argc == 2)
	{
		while (*s != '\0')
		{
			if (*s >= 'a' && *s <= 'z')
				i = *s - 'a' + 1;
			else if (*s >= 'A' && *s <= 'Z')
				i = *s - 'A' + 1;
			else
				i = 1;
			while (i)
			{
				write (1, s, 1);
				i--;
			}
			s++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
