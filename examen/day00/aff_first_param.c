#include <unistd.h>

int	main(int argc, char	*argv[])
{
	char	*s;

	if (argc < 2)
		write (1, "\n", 1);
	else
	{
		s = argv[1];
		while (*s != '\0')
		{
			write (1, s, 1);
			s++;
		}
		write (1, "\n", 1);
	}
	return (0);
}
